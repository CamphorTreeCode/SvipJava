package com.shensu.pojo;

public class ShopSkuSwiper {
	//sku的轮播图id
	private Integer shopSkuSwiperId;
	//sku的轮播图地址
	private String shopSkuSwiperUrl;
	//sku轮播图的创建时间
	private String shopSkuSwiperCreatTime;
	//skuID
	private Integer shopskuid;
	
	public Integer getShopSkuSwiperId() {
		return shopSkuSwiperId;
	}
	public void setShopSkuSwiperId(Integer shopSkuSwiperId) {
		this.shopSkuSwiperId = shopSkuSwiperId;
	}
	public String getShopSkuSwiperUrl() {
		return shopSkuSwiperUrl;
	}
	public void setShopSkuSwiperUrl(String shopSkuSwiperUrl) {
		this.shopSkuSwiperUrl = shopSkuSwiperUrl;
	}
	public String getShopSkuSwiperCreatTime() {
		return shopSkuSwiperCreatTime;
	}
	public void setShopSkuSwiperCreatTime(String shopSkuSwiperCreatTime) {
		this.shopSkuSwiperCreatTime = shopSkuSwiperCreatTime;
	}
	/**
	 * @return shopskuid
	 */
	public Integer getShopskuid() {
		return shopskuid;
	}
	/**
	 * @param shopskuid 要设置的 shopskuid
	 */
	public void setShopskuid(Integer shopskuid) {
		this.shopskuid = shopskuid;
	}

	
}
